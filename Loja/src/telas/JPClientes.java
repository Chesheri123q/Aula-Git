    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        
        //verificar campos obrigatorios
        if(jTextFieldNome.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Por favor preencha o campo nome.");
            jTextFieldNome.requestFocus();
        }else if(jTextFieldCPF.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Por favor preencha o campo CPF.");
           jTextFieldCPF.requestFocus();  
        }else{
          //cria o objeto
           Cliente cliente = new Cliente(); 
           
           //preenche os dados no objeto
           cliente.setNome(jTextFieldNome.getText());
           cliente.setCpf(jTextFieldCPF.getText());
           cliente.setTelefone(jTextFieldTelefone.getText());
           cliente.setEndereco(jTextFieldEndereco.getText());
           cliente.setBairro(jTextFieldBairro.getText());
           cliente.setCidade(jTextFieldCidade.getText());
           
           //se o codigo for vazio é um INSERT
           if(jTextFieldCodigo.getText().equals("")){
             //inserir no banco de dados
             if(repClientes.inserir(cliente) == true){
                JOptionPane.showMessageDialog(null, "Executado com sucesso!");
                limparCampos();
                preencherJTable();
             }else{
                JOptionPane.showMessageDialog(null, "Falha ao executar!"); 
             }
           }else{
            //se o código já existir então é um update   
            cliente.setId(Integer.parseInt(jTextFieldCodigo.getText()));  
            //chamamos o método para alterar os dados
             if(repClientes.atualizar(cliente) == true){
                JOptionPane.showMessageDialog(null, "Executado com sucesso!");
                limparCampos();
                preencherJTable();
             }else{
                JOptionPane.showMessageDialog(null, "Falha ao executar!"); 
             }
           
           }
            
        }        
    }//GEN-LAST:event_jButtonSalvarActionPerformed

