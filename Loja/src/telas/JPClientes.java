      public void JTablePesquisa(String valor) {
        repClientes = new RepClientes();
        DefaultTableModel modelo = (DefaultTableModel) jTableLista.getModel();
        modelo.setNumRows(0);

        for (Cliente c : repClientes.pesquisar(valor)) {
            modelo.addRow(new Object[]{
                //aqui vao ficar as colunas
                c.getId(),
                c.getNome(),
                c.getCpf(),
                c.getTelefone(),
                c.getEndereco(),
                c.getBairro(),
                c.getCidade()
            });
        }

    }  
        
