public void JTablePesquisa(String valor){
      
        DefaultTableModel modelo = (DefaultTableModel) jTableListaCurso.getModel();
        modelo.setNumRows(0);
        
        for(Curso c : repCursos.pesquisar(valor)){
            modelo.addRow(new Object[]{
                //aqui vao ficar as colunas
         c.getId(),
         c.getNome(),
         c.getValor()
        
                       
            });
        }
        
    }
