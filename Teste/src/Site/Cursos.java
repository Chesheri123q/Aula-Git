    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        if(jTextFieldNome_Curso.getText().equals("")){
            jLabel12.setText("digite um Nome!");
        }else if(jTextFieldValor_Curso.getText().equals("")){
            jLabel14.setText("digite um valor!");
        }else{
            
            
RepCursos repCurso = new RepCursos();


            Curso curso = new Curso();

            curso.setNome(jTextFieldNome_Curso.getText());
            curso.setValor(jTextFieldValor_Curso.getText());
            
                if(jTextFieldID.getText().equals("")){
                //inserir no banco de dados
                if(repCurso.inserir(curso) == true){
                    JOptionPane.showMessageDialog(null, "Executado com sucesso!");
                    limparCampos();
                    preencherJtable();
                }else{
                    JOptionPane.showMessageDialog(null, "Falha ao executar!");
                }
            }else{
                //se o código já existir então é um update
                curso.setId(jTextFieldID.getText());
                //chamamos o método para alterar os dados
                if(repCurso.atualizar(curso) == true){
                    JOptionPane.showMessageDialog(null, "Executado com sucesso!");
                    limparCampos();
                    preencherJtable();
                }else{
                    JOptionPane.showMessageDialog(null, "Falha ao executar!");
                }

            }
        }
         

        
        
    }//GEN-LAST:event_jButton1ActionPerformed

