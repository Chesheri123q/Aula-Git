    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        //verificar campos obrigatorios
        if(jTextFieldUsuario.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Por favor preencha o campo nome.");
            jTextFieldUsuario.requestFocus();
        }else if(jTextFieldCPF.getText().equals("")){
           JOptionPane.showMessageDialog(null, "Por favor preencha o campo CPF.");
           jTextFieldCPF.requestFocus();  
        }else{
          //cria o objeto
           Usuario usuario = new Usuario(); 
           
           //preenche os dados no objeto
        
        usuario.setUsuario(jTextFieldUsuario.getText());
        usuario.setNomeCompleto(jTextFieldNome_Completo.getText());
        usuario.setSenha(jTextFieldSenha.getText());
        usuario.setTelefone(jTextFieldTelefone.getText());
        usuario.setEmail(jTextFieldEmail.getText());
        usuario.setCpf(jTextFieldCPF.getText());
        usuario.setDataDeNascimento(jTextFieldData_De_Nascimento.getText());
        usuario.setEstado(jTextFieldEstado.getText());
        usuario.setCidade(jTextFieldCidade.getText());
        usuario.setRua(jTextFieldRua.getText());
           
           //se o codigo for vazio é um INSERT
           if(jTextFieldID.getText().equals("")){
             //inserir no banco de dados
             if(repUsuario.inserir(usuario) == true){
                JOptionPane.showMessageDialog(null, "Executado com sucesso!");
                limparCampos();
                preencherJtable();
             }else{
                JOptionPane.showMessageDialog(null, "Falha ao executar!"); 
             }
           }else{
            //se o código já existir então é um update   
             usuario.setId(jTextFieldID.getText());  
            //chamamos o método para alterar os dados
             if(repUsuario.atualizar(usuario) == true){
                JOptionPane.showMessageDialog(null, "Executado com sucesso!");
                limparCampos();
                preencherJtable();
             }else{
                JOptionPane.showMessageDialog(null, "Falha ao executar!"); 
             }
           
           }
            
        }  
    
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

