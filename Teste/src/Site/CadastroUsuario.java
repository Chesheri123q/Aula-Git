    public void JTablePesquisa(String valor){
      
        DefaultTableModel modelo = (DefaultTableModel) jTableListaUsuario.getModel();
        modelo.setNumRows(0);
        
        for(Usuario c : repUsuario.pesquisa(valor)){
            modelo.addRow(new Object[]{
                //aqui vao ficar as colunas
         c.getId(),
         c.getUsuario(),
         c.getNomeCompleto(),
         c.getSenha(),
         c.getTelefone(),
         c.getEmail(),
         c.getCpf(),
         c.getDataDeNascimento(),
         c.getEstado(),
         c.getCidade(),
         c.getRua()
                       
            });
        }
        
    }
         
